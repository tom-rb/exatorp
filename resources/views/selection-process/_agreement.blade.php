<div class="content">
    <h2 class="title is-5 has-text-centered">IDENTIFICAÇÃO DAS PARTES</h2>
    <h3 class="title is-5">Entidade</h3>
    <p>
        Curso Exato, com sede em Campinas, Cidade Universitária Zeferino Vaz – Barão Geraldo, Cep 13083-970,
        Estado de São Paulo.
    </p>
    <h3 class="title is-5">Voluntário</h3>
    <p>
        Pessoa física representada pelo usuário qualificado no cadastro feito por ela mesma no website
        www.cursoexato.preac.unicamp.br.
    </p>

    <p>
        <em>
            As partes acima identificadas têm, entre si, justo e acertado o presente Termo de Adesão, que se
            regerá pelas cláusulas seguintes e pelas condições descritas no presente.
        </em>
    </p>

    <h2 class="subtitle is-5 has-text-centered">DO OBJETO DO TERMO DE ADESÃO</h2>
    <p>
        <strong>Cláusula 1ª.</strong> O presente termo tem como OBJETO a regulamentação dos serviços que
        serão prestados pelo voluntário, não gerando este vínculo empregatício1, nos termos da Lei nº 9.608.
    </p>

    <h2 class="subtitle is-5 has-text-centered">DAS OBRIGAÇÕES DO VOLUNTÁRIO</h2>
    <p>
        <strong>Cláusula 2ª.</strong> O voluntário se compromete a auxiliar a entidade na função que lhe
        couber, função esta previamente acordada entre as partes.
    </p>
    <p>
        <strong>Cláusula 3ª.</strong> O voluntário se compromete a comparecer nas reuniões setoriais e
        gerais para as quais for convocado.
    </p>


    <h2 class="subtitle is-5 has-text-centered">DA EXECUÇÃO</h2>

    <p>
        <strong>Cláusula 4ª.</strong> O voluntário cumprirá 60 horas por semestre, distribuídas de acordo
        com o cargo desempenhado.
    <p>
        <strong>Cláusula 5ª.</strong> O voluntário selecionado para desempenhar a função de “professor” ou
        “monitor” deverá ter ao menos um período noturno inteiro disponível semanalmente (das 19 horas às
        22 horas e 30 minutos).
    </p>

    <h2 class="subtitle is-5 has-text-centered">DA REMUNERAÇÃO</h2>
    <p>
        <strong>Cláusula 6ª.</strong> Os serviços prestados pelo voluntário são de caráter gratuito, não
        cabendo, pois, remuneração a título de contraprestação, não havendo vínculo trabalhista e nem
        obrigação de natureza trabalhista, previdenciária ou afim.
    </p>

    <h2 class="subtitle is-5 has-text-centered">DAS OBRIGAÇÕES DA ENTIDADE</h2>
    <p>
        <strong>Cláusula 7ª.</strong> Cabe à Entidade a distribuição das tarefas para os cargos
        selecionados e a verificação de seu cumprimento.
    </p>

    <h2 class="subtitle is-5 has-text-centered">DO PRAZO</h2>
    <p>
        <strong>Cláusula 8ª.</strong> O presente termo de adesão será por prazo indeterminado.
    </p>

    <h2 class="subtitle is-5 has-text-centered">DA RESCISÃO</h2>
    <p>
        <strong>Cláusula 9ª.</strong> O presente termo poderá ser rescindido por qualquer uma das partes,
        devendo a outra parte ser comunicada com antecedência mínima de um mês.
    </p>
    <p>
        <strong>Parágrafo único:</strong> O voluntário selecionado deve informar à Entidade a possibilidade
        de rescisão contratual nos meses sequentes, quando esta houver. A possibilidade de estágio, emprego,
        intercâmbio ou qualquer outra causa que o impeça de exercer devidamente suas atividades deve ser informada com antecedência.
    </p>

    <h2 class="subtitle is-5 has-text-centered">DAS CONDIÇÕES GERAIS</h2>
    <p>
        <strong>Cláusula 8ª.</strong> O presente termo de adesão passa a vigorar a partir de sua assinatura
        pelas partes.
    </p>

    <h2 class="subtitle is-5 has-text-centered">DO FORO</h2>
    <p>
        <strong>Cláusula 9ª.</strong> Para dirimir quaisquer controvérsias oriundas deste termo de adesão,
        as partes elegem o foro da comarca de Campinas.
    </p>
</div>
